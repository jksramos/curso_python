# Aspas simples
print('Jackson Ramos')
print(1, 'Jackson "Ramos"')

# Aspas duplas
print("Jackson Ramos")
print(2, "Jackson 'Ramos'")

# Escape - "não é muito usado, pois complica o código"
print("Jackson \"Ramos\"")

#r "??"
